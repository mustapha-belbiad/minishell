#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 48 && c <= 57)
		|| (c >= 65 && c <= 90)
		|| (c >= 97 && c <= 122)
		|| (c == 95))
		return (1);
	else
		return (0);
}

int ft_isalnum2(char c)
{
	if((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= '1' && c <= '9'))
		return(0);
	return(1);
}